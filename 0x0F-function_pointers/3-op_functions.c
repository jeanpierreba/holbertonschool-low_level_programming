#include "3-calc.h"

/**
 * op_add - sum two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of two integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two integers
 * @a: first integer
 * @b: second integer
 * Return: sub of two integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product of two integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - result of the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: result of two integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the module of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of the division of two integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
